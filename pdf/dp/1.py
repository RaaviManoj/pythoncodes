val=[6,10,12]
wt=[1,2,3]
c=5

n=len(val)

dp=[[-1 for i in range(c+1)]for j in range(n+1)]

def knap(n,c):
    global dp
    if n==-1 or c==0:return 0
    if dp[n][c]!=-1:return dp[n][c]
    if wt[n]<=c:
        dp[n][c]=max(knap(n,c-wt[n])+val[n],knap(n-1,c))
        return dp[n][c]
    else:
        dp[n][c]=knap(n-1,c)
        return dp[n][c]
    
